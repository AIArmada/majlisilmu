=== Quick Reference Guide ===

When to use what:

ATTRIBUTES:
- #[Computed] - For expensive calculations that should be memoized
- #[Lazy] - Load component after initial page render
- #[Defer] - Load component immediately after page load (before viewport)
- #[Async] - Fire-and-forget actions that don't need to wait for response
- #[Renderless] - Actions that don't trigger re-renders
- #[Locked] - Prevent client-side property manipulation
- #[Validate] - Component-level validation rules
- #[Url] - Sync property with URL query string
- #[Session] - Persist property in session
- #[Title] - Set page title dynamically
- #[Layout] - Specify layout for full-page components
- #[Reactive] - Re-render when parent property changes
- #[Modelable] - Allow parent to bind to child property
- #[On] - Listen for events
- #[Js] - Execute JavaScript after component updates
- #[Isolate] - Prevent parent re-renders from affecting child
- #[Transition] - CSS transitions during updates

DIRECTIVES:
- @placeholder - Loading skeleton while lazy/defer components load
- @island - Isolated Livewire components in non-Livewire pages
- @persist - Keep DOM elements across navigation
- @teleport - Render content in different DOM location

WIRE DIRECTIVES:
- wire:model - Two-way data binding
- wire:model.live - Real-time binding (updates on every keystroke)
- wire:model.blur - Update on blur
- wire:model.change - Update on change
- wire:model.debounce.500ms - Debounced updates
- wire:click - Handle click events
- wire:submit - Handle form submission
- wire:loading - Show/hide during requests
- wire:dirty - Show when form is modified
- wire:navigate - SPA-like navigation
- wire:key - Unique identifier for loop items (IMPORTANT!)
- wire:init - Run action when component loads
- wire:poll.5s - Poll server every 5 seconds
- wire:confirm - Confirm before action
- wire:intersect - Trigger when element enters viewport

BEST PRACTICES:
1. Always use wire:key in loops
2. Use #[Computed] for Eloquent queries
3. Use lazy/defer for below-fold content
4. @placeholder must match component's root element type
5. Single root element required for all components
6. Avoid N+1 queries with eager loading
7. Cache expensive operations
8. Use #[Async] for side-effect operations
9. Use #[Renderless] when UI doesn't need to update
10. Test components with Livewire::test()

COMMON PATTERNS:
- Single-file components: resources/views/⚡component.blade.php
- Multi-file: app/Livewire/Component.php + resources/views/livewire/component.blade.php
- Full-page: Route::livewire('/path', 'component-name')
- Form validation: #[Validate] or $rules property
- Real-time validation: wire:model.live with validation
- Loading states: wire:loading, wire:target
- Conditional visibility: style="display:none" (not @if for root)

